#include "../include/minishell.h"

void	print_tab(char **tab)
{
	int		i;

	if (tab)
	{
		i = 0;
		while (tab[i] != NULL)
		{
			ft_putstr(tab[i]);
			ft_putstr("\n");
			i++;
		}
	}
}
